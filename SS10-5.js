// Dùng hàm prompt() để nhập 2 số từ bàn phím, lưu vào 2 biến là firstNumber và secondNumber. Kiểm tra xem firstNumber có phải là bội số của secondNumber hay không (tức là firstNumber có chia hết cho secondNumber hay không). Hiển thị kết quả ra màn hình bằng cách sử dụng hàm alert().

let firstNumber = prompt("Nhập vào số thứ nhất");
let secondNumber = prompt("Nhập vào số thứ hai");

let result = firstNumber / secondNumber;

alert(result);